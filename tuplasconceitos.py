#tuplas
#Tuplas são definidas pela virgula e não pelo uso dos parenteses
#Métodos para adição e remoção de elementos de tupla não existem
#Tuplas são representadas por parenteses mas podem ser escritas dos dois jeitos:
tupla1 = (1, 2, 3, 4, 5, 6)
print(tupla1)
print(type(tupla1))

tupla2 = 1, 2, 3, 4, 5, 6
print(tupla2)
print(type(tupla2))

#Tuplas com 1 elemento - certo
tupla4 = (4,)  #é uma tupla
print(tupla4) 
print(type(tupla4))

#Tuplas com 1 elemento - errado
tupla3 = (3)  #Não é uma tupla
print(tupla3) 
print(type(tupla3))


